print('Entre com o seu nome: ') # Exibe um texto para o Usuario
nome = gets() # Define uma variavel e guarda o valor informado nela, gets pega o que foi capturado, captura o que o usuario informou
puts("Bem-vindo #{nome}") # Exibe um texto junto de uma variavel

# Diferenca de Print para Puts

=begin
    print -> Comando que nao quebra a linha
    puts -> Comando que ira quebrar a linha
    Nas funcoes nao e necessario usar parenteses
=end